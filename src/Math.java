public class Math {

    public static void add(double a, double b){

        System.out.println("Svar: " + (a + b));
        System.out.println();
    }
    public static void subtract(double a, double b){

        System.out.println("Svar: " + (a - b));
        System.out.println();
    }
    public static void multiply(double a, double b){

        System.out.println("Svar: " + a * b);
        System.out.println();
    }
    public static void divide(double a, double b){
        System.out.println("Svar: " + a/b);
        System.out.println();
    }
    public static void modulus(double a, double b){
        System.out.println("Svar: " + a%b);
        System.out.println();
    }
    public static void biggerThan(double a, double b){
        System.out.println("Svar: " + (a>b));
        System.out.println();
    }
    public static void smallerThan(double a, double b){
        System.out.println("Svar: " + (a<b));
        System.out.println();
    }
    public static void equals(double a, double b){
        System.out.println("Svar: " + (a==b));
        System.out.println();
    }

}
